angular.module('starter.vocabulary', [])
 .filter('wordsByLength', function () {
        return function (array, length) {
            if (isNaN(length))
                length = 3;

            var result = [];
            var j = 0;
            for (var i = 0; i < array.length; i++) {
            	if (array[i].length == length) {
            		result[j] = array[i];
            		j++;
            	}
            };
	        return result;  
    }
})
